package String;

public class pattern {

	public static void main(String[] args) {

    for(int i=1;i<=4;i++) {
    	for(int j=3;j>=i;j--) {
    		System.out.print(" ");
    	}System.out.println(" *");
    }
    for(int a=1;a<=3;a++) {
    	for(int b=1;b<=a;b++) {
    		System.out.print(" ");
    	}System.out.println(" *");
    }

	}

}
